import math

print(round(1.5))#te devuelve el numero mas cercano
print(abs(-77))#si el numero es negativo te lo devuelve positivo
print(abs(77))#si el numero es positivo te lo devuelve negativo

print(math.ceil(1.1))#te devuelve el numero mas cercano (tiene que ser mas grande)
print(math.floor(1.999999))#te devuelve el numero mas cercano (tiene que ser mas pequeno)
print(math.isnan(23))#si el valor es un numero va a decir false
#print(math.isnan("23"))
print(math.pow(10, 3))#para la potencia
print(math.sqrt(9))#raiz cuadrada
